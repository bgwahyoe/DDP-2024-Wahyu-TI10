import math

def tambah(a, b):
  # mengembalikan nilai penjumlahan a dan b
  return a + b

def kurang(a, b):
  # mengembalikan nilai pengurangan a dan b
  return a - b

def kali(a, b):
  # mengembalikan nilai perkalian a dan b
  return a * b

def bagi(a, b):
  # mengembalikan nilai pembagian a dan b
  return a / b

def pangkat(a, b):
  # mengembalikan nilai a pangkat b
  return math.pow(a, b)

def akar(a, b):
  # mengembalikan nilai akar pangkat b dari a
  return math.sqrt(a, b)

def mod(a, b):
  # mengembalikan nilai sisa hasil bagi a dan b
  return a % b

def floor(a, b):
  # mengembalikan nilai floor dari a dan b
  return math.floor(a, b)

def ceil(a, b):
  # mengembalikan nilai ceil dari a dan b
  return math.ceil(a, b)

def abs(a, b):
  # mengembalikan nilai absolut dari a
  return math.fabs(a, b)

def max(a, b):
  # mengembalikan nilai yang paling besar antara a dan b
  return max(a, b)

def min(a, b):
  # mengembalikan nilai yang paling kecil antara a dan b
  return min(a, b)

def round(a, b):
  # mengembalikan nilai pembulatan a sampai dengan b angka dibelakang koma
  return round(a, b)